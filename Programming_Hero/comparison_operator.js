/*
    Comparison
    1. greater: >
    2. less: <
    3. equal: ==
    4. greater than or equals to: >=
    5. less than or equals to: >=
    6. not equal: !=

    combining more than one conditions
    7. and: &&
    8. or: ||
*/

var a = 5;
var b = 10;
console.log(a > b);
console.log(a < b);
console.log(a == b);
console.log(a >= b);
console.log(a <= b);
console.log(a != b);