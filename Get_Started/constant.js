const pie = 3.1416;
console.log(pie);

// pie = 7.3;
// We can't reassign a constant. it will show an Type Error message.

/*

Quick QnA

1. What is the purpose of a constant?
ans: The purpose of a constant is to define data that can't be changed.

2. what is the difference between a constant and a variable?
ans: A variable can store data that can be changed and also reassign later.
A constant can't store data that can be changed and also can't reassign later.

*/