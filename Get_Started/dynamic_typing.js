/*
    Dynamic Typing
    --------------

    We can assign multiple types of data to a single variable, and it will work perfectly. This is because JavaScript is dynamically typed.

    However, this can cause problems at a professional level. That's why we use TypeScript instead of JavaScript.
*/

var firstName = "Mehedi"
console.log(typeof firstName);
firstName = 100;
console.log(typeof firstName);
firstName = true;
console.log(typeof firstName);

